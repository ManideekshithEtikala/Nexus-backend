class Neo4jConnection:
    def __init__(self):
        # Initialize the asynchronous driver
        self.driver = AsyncGraphDatabase.driver(NEO4J_URI, auth=(NEO4J_USER, NEO4J_PASSWORD))

    async def close(self):
        await self.driver.close()
    
    async def fetch_user_graph_facts(self) -> str:
        """
        Fetches all relationships connected to the User to inject into the ReAct prompt.
        Because Nexus is a personal agent, we fetch all facts radiating from the 'User' node.
        """
        async with self.driver.session() as session:
            # MATCH any node connected to the User and return the relationship and the target node
            query = """
            MATCH (u:User)-[r]->(target)
            RETURN type(r) AS relation, target.name AS target_name, labels(target)[0] AS target_type
            """
            result = await session.run(query)
            records = await result.data()

            if not records:
                return "No permanent facts established yet."

            # Format the raw graph data into human-readable sentences for the LLM
            facts = []
            for row in records:
                # Example output: "User IS_BUILDING Nexus (Type: Project)"
                facts.append(f"User {row['relation']} {row['target_name']} (Type: {row['target_type']})")

            return "\n".join(facts)

    async def execute_graph_update(self, graph_data: dict):
        """
        Takes the dictionary generated by the Open Ontology LLM schema
        and executes the MERGE commands safely using parameterized queries.
        """
        async with self.driver.session() as session:
            # 1. Merge all Nodes dynamically
            for node in graph_data.get("nodes", []):
                # The entity_type is dynamic (e.g., 'Dog', 'Technology')
                query = f"""
                MERGE (n:{node['entity_type']} {{name: $name}})
                """
                await session.run(query, name=node['entity_name'])

            # 2. Merge all Edges dynamically
            if "edges" in graph_data:
                for edge in graph_data["edges"]:
                    # 1. Grab the raw relation string from the LLM
                    raw_relation = edge.get("relation", "RELATED_TO")
                    
                    # 2. SANITIZE IT: Uppercase it and replace all spaces/dashes with underscores
                    safe_relation = raw_relation.upper().replace(" ", "_").replace("-", "_")

                    # 3. Use the safe_relation in the Cypher query
                    query = f"""
                    MATCH (source {{id: $source_id}})
                    MATCH (target {{id: $target_id}})
                    MERGE (source)-[r:{safe_relation}]->(target)
                    """
                    await session.run(
                        query,
                        source_id=edge["source"],
                        target_id=edge["target"]
                    )
                
# Instantiate a singleton client for FastAPI to use
